export const getUiMessages = (language?: string | null): UiMessages => messages[normalizeAppLanguage(language)];

export const formatLastOpened = (value?: string | null, language?: string | null): string => {
  const locale = normalizeAppLanguage(language);
  if (!value) return getUiMessages(locale).noUsage;

  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return getUiMessages(locale).noUsage;

  return new Intl.DateTimeFormat(locale, {
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit',
  }).format(date);
};

export const localizeErrorMessage = (message: string, language?: string | null): string => {
  const locale = normalizeAppLanguage(language);
  if (locale === 'zh-CN') return message;

  const exactMatch = exactErrorMessages[locale]?.[message];
  if (exactMatch) return exactMatch;

  for (const rule of prefixedErrorMessages[locale] ?? []) {
    if (message.startsWith(rule.from)) {
      return `${rule.to}${message.slice(rule.from.length)}`;
    }
  }

  return message;
};

